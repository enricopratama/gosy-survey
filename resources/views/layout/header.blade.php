<script>
    document.addEventListener('DOMContentLoaded', function() {
        const userControlsButton = document.getElementById('user-controls-button');
        const userControlsContent = document.querySelector('.user-controls-content');

        userControlsButton.addEventListener('click', function() {
            userControlsContent.style.display = userControlsContent.style.display === 'block' ? 'none' : 'block';
        });

        document.addEventListener('click', function(event) {
            if (!userControlsButton.contains(event.target) && !userControlsContent.contains(event.target)) {
                userControlsContent.style.display = 'none';
            }
        });
    });
</script>